export const getDifficultyFromPassScore = (
  passScore?: number | null
): "easy" | "medium" | "hard" => {
  if (!passScore) return "medium";
  if (passScore < 60) return "easy";
  if (passScore < 80) return "medium";
  return "hard";
};

export const getDifficultyColor = (difficulty: string) => {
  switch (difficulty) {
    case "easy":
      return {
        bg: "bg-green-100",
        text: "text-green-700",
        border: "border-green-300",
      };
    case "medium":
      return {
        bg: "bg-yellow-100",
        text: "text-yellow-700",
        border: "border-yellow-300",
      };
    case "hard":
      return {
        bg: "bg-red-100",
        text: "text-red-700",
        border: "border-red-300",
      };
    default:
      return {
        bg: "bg-gray-100",
        text: "text-gray-700",
        border: "border-gray-300",
      };
  }
};

export const getDifficultyLabel = (difficulty: string) => {
  switch (difficulty) {
    case "easy":
      return "Dễ";
    case "medium":
      return "Trung bình";
    case "hard":
      return "Khó";
    default:
      return difficulty;
  }
};

